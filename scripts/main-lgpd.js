function abreInfo1() {
    var x = document.getElementById("data-answer-1");
    var y = document.getElementById("logo-1")
    var z = document.getElementById("title-1")
    
    if (x.style.display !== "block") {
        x.style.display = "block";
        y.style.color = "#2E3092";
        z.style.background = "#2E3092";
        z.style.border = "1px solid #2E3092";

    } else {
        x.style.display = "none";
        y.style.color = "#1F1F1F";
        z.style.background = "#1F1F1F";
        z.style.border = "1px solid white";
    }
}

